# Assignment Operators Example

x = 10
print("Initial value of x:", x)

# += (Add and assign)
x += 5
print("After x += 5:", x)  # 15

# -= (Subtract and assign)
x -= 3
print("After x -= 3:", x)  # 12

# *= (Multiply and assign)
x *= 2
print("After x *= 2:", x)  # 24

# /= (Divide and assign)
x /= 4
print("After x /= 4:", x)  # 6.0

# %= (Modulus and assign)
x %= 3
print("After x %= 3:", x)  # 0.0

# **= (Exponent and assign)
x **= 2
print("After x **= 2:", x)  # 0.0

# //= (Floor division and assign)
x //= 2
print("After x //= 2:", x)  # 0.0
